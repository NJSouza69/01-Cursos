#Operador OU
print ("(1 > 2) | (3 < 4) ?", (1>2)|(3<4))
print ("(1 > 2) or (3 < 4) ?", (1>2)|(3<4))

#Operador E
print ("(1 > 2) & (3 < 4) ?", (1>2)&(3<4))
print ("(1 > 2) and (3 < 4) ?", (1>2)and(3<4))

#Operador not
print ("not((1 > 2) and (3 < 4)) ?", not((1>2)and(3<4)))
print ("not((1 > 2) or (3 < 4)) ?", not((1>2)or(3<4)))

